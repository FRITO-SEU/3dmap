package app.core;

public class Double4 {
    public Double4() {
    }

    public Double4(double initX, double initY, double initZ, double initW) {
        x = initX;
        y = initY;
        z = initZ;
        w = initW;
    }

    public double x;
    public double y;
    public double z;
    public double w;
}