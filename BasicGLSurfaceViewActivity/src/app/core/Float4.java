package app.core;

public class Float4 {
    public Float4() {
    }

    public Float4(float initX, float initY, float initZ, float initW) {
        x = initX;
        y = initY;
        z = initZ;
        w = initW;
    }

    public float x;
    public float y;
    public float z;
    public float w;
}